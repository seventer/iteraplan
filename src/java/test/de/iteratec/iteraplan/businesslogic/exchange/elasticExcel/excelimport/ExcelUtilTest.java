package de.iteratec.iteraplan.businesslogic.exchange.elasticExcel.excelimport;

import static org.junit.Assert.assertEquals;

import org.apache.poi.hssf.usermodel.HSSFWorkbook;
import org.apache.poi.ss.usermodel.Cell;
import org.apache.poi.ss.usermodel.Row;
import org.apache.poi.ss.usermodel.Sheet;
import org.apache.poi.ss.usermodel.Workbook;
import org.apache.poi.xssf.usermodel.XSSFWorkbook;
import org.junit.Test;

import de.iteratec.iteraplan.businesslogic.exchange.elasticExcel.util.ExcelUtils;


/**
 * Tests for {@link ExcelUtils}
 */
public class ExcelUtilTest {

  @Test
  public void testGetFullCellNameExcel2003() {
    testGetFullCellName(new HSSFWorkbook());
  }

  @Test
  public void testGetFullCellNameExcel2007() {
    testGetFullCellName(new XSSFWorkbook());
  }

  private void testGetFullCellName(Workbook wb) {

    Sheet sheet1 = wb.createSheet("Sheet-1");
    Row row1 = sheet1.createRow(0);
    Cell cell1 = row1.createCell(0);

    assertEquals("Sheet-1 A-1", ExcelUtils.getFullCellName(cell1));
    assertEquals("<no-cell>", ExcelUtils.getCellRefName(null));
  }

}
