package de.iteratec.iteraplan.model.queries;

import de.iteratec.iteraplan.model.interfaces.IdEntity;


/**
 * Model class for meta information about files stored within iteraplan.
 * 
 * @author Gunnar Giesinger, iteratec GmbH, 2007
 */
public abstract class SavedQueryEntity implements IdEntity {

  private static final long serialVersionUID = 7898459488042407568L;
  private Integer           id;
  private Integer           olVersion;
  /** The name of the file as displayed in the GUI. Internally the URI is used to access files!! */
  private String            name;
  private String            description;
  private String            content;

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }

  public Integer getOlVersion() {
    return olVersion;
  }

  public void setOlVersion(Integer olVersion) {
    this.olVersion = olVersion;
  }

  public String getName() {
    return name;
  }

  public String getDescription() {
    return description;
  }

  public void setName(String name) {
    this.name = name;
  }

  public void setDescription(String description) {
    this.description = description;
  }

  public String getContent() {
    return content;
  }

  public void setContent(String content) {
    this.content = content;
  }

  abstract void validate();

}
